    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ImageIcon icon=new ImageIcon("images/login.png");
        Image img=icon.getImage();
        Image newimg=img.getScaledInstance(24, 22, java.awt.Image.SCALE_SMOOTH);
        ImageIcon finale=new ImageIcon(newimg);
        profile.setIcon(finale);
        
        table.setShowHorizontalLines(true);
        table.setShowVerticalLines(true);
        table.setAlignmentX(Component.CENTER_ALIGNMENT);
        
        String head[]={"No.","Date","Comment","Update","Remove"};
        DefaultTableModel dtm=new DefaultTableModel(head,0);
        
        JTable tbl=new JTable(dtm);
        
        String str="SELECT * FROM notes";
            try
            {
                LocalDate ld;
                LocalTime lt;
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection con = DriverManager.getConnection(url);
                Statement stmt=con.createStatement();
                
                ResultSet rs=stmt.executeQuery(str);
                while(rs.next())
                {
                    ld=rs.getDate("posted").toLocalDate();
                    lt=rs.getTime("posted").toLocalTime();
                    String sum=ld+" "+lt;
                    String[] item={Integer.toString(rs.getInt("note")),sum,rs.getString("comment"),"+","x"};
                    dtm.addRow(item);
                }
                table.setVisible(true);
                table.setModel(dtm);
                
                table.addMouseListener(new java.awt.event.MouseAdapter() {
                    public void mouseClicked(java.awt.event.MouseEvent evt) {
                        int row=table.getSelectedRow();
                        int col=table.getSelectedColumn();
                        if(col==0 || col==1)
                        {
                            JOptionPane.showMessageDialog(rootPane, "Not editable");
                        }
                        Object noteNo;
                        Object com;
                        
                        if(col==3)    //update
                        {
                            noteNo=table.getValueAt(row, 0);
                            com=table.getValueAt(row, 2);
                            try
                            {
                                String upd="UPDATE notes SET comment='"+com+"', posted='"+LocalDateTime.now()+"' WHERE note='"+noteNo+"'";
                                int ac=stmt.executeUpdate(upd);
                                if(ac==1)
                                {
                                    new Reports().setVisible(true);
                                    dispose();
                                }
                                else
                                {
                                    JOptionPane.showMessageDialog(rootPane, "Updating error");
                                }
                            }
                            catch(Exception ex)
                            {
                                ex.printStackTrace();
                            }
                        }
                        else if(col==4) //remove
                        {
                            noteNo=table.getValueAt(row, 0);
                            try
                            {
                                String del="DELETE FROM notes WHERE note='"+noteNo+"'";
                                int ab=stmt.executeUpdate(del);
                                if(ab==1)
                                {
                                    new Reports().setVisible(true);
                                    dispose();
                                }
                                else
                                {
                                    JOptionPane.showMessageDialog(rootPane, "The note doesn't exist");
                                }
                            }
                            catch(Exception ex)
                            {
                                ex.printStackTrace();
                            }
                        }
                    }
                });
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
    }//GEN-LAST:event_formComponentShown


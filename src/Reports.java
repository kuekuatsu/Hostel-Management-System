    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String comment=input.getText();
        LocalDateTime ldt=LocalDateTime.now();
        //System.out.println(ldt);
        if(comment.equals("") || comment.equals("Add Report / Note"))
        {}
        else
        {
            try
            {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection con = DriverManager.getConnection(url);
                Statement stmt=con.createStatement();

                String s="INSERT INTO notes(posted, comment) VALUES('"+ldt+"','"+comment+"')";
                int a=stmt.executeUpdate(s);
                if(a==1)
                {
                    new Reports().setVisible(true);
                    dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "Some insertion error occurred");
                }
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_saveBtnActionPerformed

